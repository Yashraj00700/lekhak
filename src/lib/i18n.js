/**
 * Translate a key. Falls back to: en → key.
 */
export function translate(lang, key, vars) {
  const dict = STRINGS[lang] || STRINGS[DEFAULT_LANG] || {};
  let value = dict[key];
  if (value === undefined) value = STRINGS.en[key];
  if (value === undefined) return key;
  if (vars && typeof value === 'string') {
    return value.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? String(vars[k]) : `{${k}}`));
  }
  return value;
}

/**
 * Detect a sensible default from the browser. We default to Marathi
 * (the author's native language) — but if the browser is clearly
 * English-only (no Marathi locale), open with English so the first-run
 * is approachable for whoever opens it.
 */
export function detectDefaultLang() {
  if (typeof navigator === 'undefined') return DEFAULT_LANG;
  const langs = [navigator.language, ...(navigator.languages || [])].filter(Boolean);
  for (const l of langs) {
    if (typeof l !== 'string') continue;
    if (l.toLowerCase().startsWith('mr')) return 'mr';
  }
  // English speakers see English first; Marathi readers can always toggle.
  return 'en';
}

export function localeFor(lang) {
  return lang === 'mr' ? 'mr-IN' : 'en-IN';
}

export function formatNumber(value, lang) {
  try {
    return new Intl.NumberFormat(localeFor(lang)).format(value);
  } catch {
    return String(value);
  }
}

export function formatDate(ts, lang) {
  if (!ts) return '';
  try {
    const d = new Date(ts);
    const today = new Date();
    const sameDay =
      d.getFullYear() === today.getFullYear() &&
      d.getMonth() === today.getMonth() &&
      d.getDate() === today.getDate();
    const time = d.toLocaleTimeString(localeFor(lang), { hour: '2-digit', minute: '2-digit' });
    if (sameDay) return translate(lang, 'home.todayPrefix') + time;
    return d.toLocaleDateString(localeFor(lang), { day: 'numeric', month: 'short', year: 'numeric' });
  } catch {
    return new Date(ts).toLocaleDateString();
  }
}
